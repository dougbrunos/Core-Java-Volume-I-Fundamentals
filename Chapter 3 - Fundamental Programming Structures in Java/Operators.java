public class Operators {

    public static void main(String[] args) {
        // Take the square root of a number, use the sqrt method:
        double x = 16;
        double y = Math.sqrt(x);
        System.out.println(y);

        double base = 4;
        double exponent = 2;
        // The Math.pow() method returns the value of the base raised to the power of
        // the exponent as a double
        double pow = Math.pow(base, exponent);
        System.out.println(pow);
    }

}